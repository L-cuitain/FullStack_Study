//10.一张纸的厚度大约是0.08mm，对折多少次之后能达到珠穆朗玛峰的高度（8848.13米）？

//分析:
//纸张厚度为0.08 对折一次厚度为 0.08*2
//创建循环 循环0.08*2 并将结果重新赋值 记录最后能循环多少次 能大于或等于8848.13



//定义变量paper 初始值为 0.08
var paper = 0.08;
//定义次数  初始值为0
var count = 0;

//创建while循环  当paper 小于 8848.13时执行循环体
while(paper <= 8848.13){
    //每次循环paper*2
    paper *= 2;
    //次数自增
    count++;
}
//打印
console.log("对折"+count+"次之后能达到珠穆朗玛峰高度");